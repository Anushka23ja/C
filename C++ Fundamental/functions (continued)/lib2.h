#include<iostream>
using namespace std;
void calsum(int x, int y)
{
    int res;
    res= x+y;
    cout<<"\nSum: "<<res;
}
void calsub(int x, int y)
{
    int res;
    res= x-y;
    cout<<"\nSub: "<<res;
}
void calmul(int x, int y)
{
    int res;
    res= x*y;
    cout<<"\nMulti: "<<res;
}
void caldiv(int x, int y)
{
    int res;
    res= x/y;
    cout<<"\nDiv: "<<res;
}